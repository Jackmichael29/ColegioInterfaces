    public Usuario RecibirDatos(){
        Usuario u = new Usuario();
        
        try {
            u.setNombres(jTextNombres.getText());
            u.setApellido_paterno(jTextApellidoPaterno.getText());
            u.setApellido_materno(jTextApellidoMaterno.getText());
            if(Inicio.dniUsuarios.contains(jTextDni.getText())){
                u.agregarErrores("dniI","El dni ingresado ya pertenece a otro usuario");
            }
            u.setDni(jTextDni.getText());
            u.setRol(jTextContacto.getText());
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        
        if(!jlID.getText().isEmpty())
            u.setUsuario_id(Integer.parseInt(jlID.getText()));

        LimpiarErrores();
        
        return u;
    }
    
    public void Actualizar(Usuario user){
        UsuarioBO uBO = new UsuarioBO();
        
        if(user.getErrores().isEmpty()){
            try {
                uBO.actualizar(user);
                dispose();
            } catch (Exception e) {
                if ( e.getMessage().contains("El DNI ingresado ya existe") ) {
                    jLabelDni.setText("El DNI ingresado ya fue registrado");
                }
                System.out.println(e.getMessage());
            }
        }

        else{
            ImprimirErrores(user.getErrores());
        }
    }
    
