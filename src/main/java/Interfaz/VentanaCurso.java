    private void jbPrincipalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPrincipalActionPerformed
        // TODO add your handling code here:
        Curso curso = RecibirDatos();
        
        if(jbPrincipal.getText().equals("Insertar"))
            Insertar(curso);
        
        else if(jbPrincipal.getText().equals("Actualizar"))
            Actualizar(curso);
    }//GEN-LAST:event_jbPrincipalActionPerformed

    private Curso RecibirDatos(){
        Curso curso = new Curso();
        curso.setNombre(jTextNombres.getText());
        
        if(!(jComboBoxGrado.getSelectedIndex() == 0))
            curso.setGrado(jComboBoxGrado.getSelectedItem().toString().charAt(0));
        else
            curso.setGrado(' ');
        
        if(!(jComboBoxNivel.getSelectedIndex() == 0))
            curso.setNivel(jComboBoxNivel.getSelectedItem().toString().charAt(0));
        else
            curso.setNivel(' ');
        
        if(!jlID.getText().isEmpty())
            curso.setCurso_id(Integer.parseInt(jlID.getText()));
        
        LimpiarErrores();
        try {
            curso.setArea_id(Integer.parseInt( jTextAreaId.getText() ));
        } catch (Exception e) {
            System.out.println(e.getMessage());
            jLabelAreaId.setText("Ingresa un número en el campo código área");
        }
        
        return curso;
    }
    
    private void Insertar(Curso curso){
        CursoBO cursoBO = new CursoBO();
        
        if(curso.getErrores().isEmpty()){
            try {
                cursoBO.insertar(curso);
                
                dispose();
            } catch (Exception e) {
                if("El id del area no es válido".equals(e.getMessage()))
                    jLabelAreaId.setText("El id del area no es válido");
                System.out.println(e.getMessage());
            }
        }
        
        else{
            ImprimirErrores(curso.getErrores());
        }
    }
    
    private void Actualizar(Curso curso){
        CursoBO cursoBO = new CursoBO();
        
        if(curso.getErrores().isEmpty()){
            try {
                cursoBO.actualizar(curso);
                
                dispose();
            } catch (Exception e) {
                if("El id del area no es válido".equals(e.getMessage()))
                    jLabelAreaId.setText("El id del area no es válido");
                System.out.println(e.getMessage());
            }
        }
        
        else{
            ImprimirErrores(curso.getErrores());
        }
    }
    
