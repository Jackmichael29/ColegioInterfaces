    private void jbPrincipalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPrincipalActionPerformed
        // TODO add your handling code here:
        jLabelError.setText("");

        DocenteCurso docCurso = new DocenteCurso();
        DocenteCursoBO docenteCursoBO = new DocenteCursoBO();

        try {
            if (jTextTurno.getText().isEmpty()) {
                jLabelError.setText("El campo parentesco no debe estar vacía");
                return;
            }

            docCurso.setTurno(jTextTurno.getText().charAt(0));
            docCurso.setDocente_id(docente_id);
            docCurso.setCurso_id(Integer.parseInt(jTextCursoId.getText()));
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }

        try {
            docenteCursoBO.insertar(docCurso);

            dispose();
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
            if(ex.getMessage().equals("El turno para ese curso ya está registrado")){
                jLabelError.setText(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jbPrincipalActionPerformed

