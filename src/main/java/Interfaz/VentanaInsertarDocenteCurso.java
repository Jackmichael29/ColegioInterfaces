    private DefaultTableModel modeloCurso;
    private ArrayList<Curso> cursos;
    private CursoBO cursoBO = new CursoBO();
    
    private void actualizarTablaCurso(){
        modeloCurso = new DefaultTableModel();
        
        jTableCurso.setModel(modeloCurso);
        
        modeloCurso.addColumn("Curso");
        modeloCurso.addColumn("Grado");
        modeloCurso.addColumn("Nivel");
        modeloCurso.addColumn("Area");
        
        try {
            System.out.println(cursos.size());
            for (int i = 0; i < cursos.size(); i++) {
                Curso curso = cursos.get(i);
                String nombre_curso = curso.getNombre()+ " ("+ String.valueOf(curso.getCurso_id())+ ")"; 
                String nombre_area = curso.getArea().getArea_nombre()+ " ("+ String.valueOf(curso.getArea_id())+ ")"; 
                
                Object [] fila = new Object[] {nombre_curso,
                    curso.getGrado(), curso.getNivel(),
                    nombre_area};
                modeloCurso.addRow(fila);
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
