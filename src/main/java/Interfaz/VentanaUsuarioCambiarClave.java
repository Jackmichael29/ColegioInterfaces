    public void Actualizar(Usuario user) throws Exception{
        LimpiarErrores();
        UsuarioBO uBO = new UsuarioBO();
        Encriptador en = new Encriptador();
        System.out.println(pswActual.getText());
        if(pswActual.getText().isBlank()){
            user.agregarErrores("claveActual", "Debe digitar la clave anterior");
        }
        if(pswNuevo.getText().isBlank()){
            System.out.println("llego hasta aqui");
            user.agregarErrores("claveNueva", "Debe digitar una clave nueva");
        }
        if(user.getErrores().containsKey("claveActual") || user.getErrores().containsKey("claveActual")){
            LimpiarErrores();
            System.out.println("Debe imprimir");
            ImprimirErrores(user.getErrores());
        }else{
            if (en.Encriptar(pswActual.getText()).equals(user.getClave())) {
                System.out.println("len detextado::: === " + pswNuevo.getText().length());
                if(pswNuevo.getText().length()>=8){
                    try {
                        uBO.cambiarClave(user, en.Encriptar(pswNuevo.getText()));
                        dispose();
                    } catch (Exception e) {
                        System.out.println(e.getMessage());
                    }
                }else{
                    user.agregarErrores("claveLenNueva", "La clave de tener al menos 8 digitos");
                    ImprimirErrores(user.getErrores());
                    user.limpiarErrores();
                }
            } else {
                user.agregarErrores("clave", "La clave es incorrecta");
                ImprimirErrores(user.getErrores());
                user.limpiarErrores();
            }
        }
    }
    
