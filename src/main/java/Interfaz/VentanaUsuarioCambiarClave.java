    private void ImprimirErrores(Map<String, String> errores){    
        for (String error : errores.keySet().toArray(new String[0])) {
            switch (error) {
                case "clave" -> lblClaveActual.setText(errores.get(error));
                case "claveActual" -> lblClaveActual.setText(errores.get(error));
                case "claveNueva" -> lblClaveNueva.setText(errores.get(error));
                case "claveLenNueva" -> lblClaveNueva.setText(errores.get(error));
            }
            System.out.println("ERRORES DETECTADOS: =====");
            System.out.println(error);
        }
    }
    
    private void LimpiarErrores(){
        lblClaveActual.setText("-");
        lblClaveNueva.setText("-");
    }
    
